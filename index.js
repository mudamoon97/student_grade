var msg = 'enter the grade of student:';

var grade = prompt(msg);


if (grade >=99){
    console.log('Distinction')
}else if(grade >=89 ){
    console.log('Exellent')
}else if (grade >= 70){
    console.log('Very Good')
}else if(grade >=60){
    console.log('Good')
}else if(grade >=50){
    console.log('Average')
}else if(grade >= 49){
    console.log('Pass')
}else if(grade >=40){
    console.log('Fail')

}else
{
    console.log('Input not accepted')
}




   